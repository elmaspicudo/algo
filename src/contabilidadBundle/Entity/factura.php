<?php

namespace contabilidadBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * factura
 */
class factura
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var binary
     */
    private $total;

    /**
     * @var string
     */
    private $uuID;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set total
     *
     * @param binary $total
     * @return factura
     */
    public function setTotal($total)
    {
        $this->total = $total;

        return $this;
    }

    /**
     * Get total
     *
     * @return binary 
     */
    public function getTotal()
    {
        return $this->total;
    }

    /**
     * Set uuID
     *
     * @param string $uuID
     * @return factura
     */
    public function setUuID($uuID)
    {
        $this->uuID = $uuID;

        return $this;
    }

    /**
     * Get uuID
     *
     * @return string 
     */
    public function getUuID()
    {
        return $this->uuID;
    }
}
